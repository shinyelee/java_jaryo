package chapter1_1;

public class Hello { // Java 프로그램은 하나 혹은 그 이상의 클래스로 구성된다. 이 프로그램은 Hello라는 이름의 하나의 클래스로 이뤄졌다.
					 // 클래스명은 대문자로 짓는 게 국룰이다. 들여쓰기도 국룰이다.
	public static void main(String[] args) { 	// main method는 프로그램 실행이 시작되는 곳이다.
		System.out.println( "Hello Java !!!" ); // 문자열을 큰따옴표로 묶어 화면에 출력한다.
												// 큰따옴표로 둘러싸인 문자들은 하나의 String 데이터로 취급한다.
	}

}

// 자동완성 : ctl+space
